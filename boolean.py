# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (True) ou não (False).

# As respostas True e False têm letra maiúscula no início!!!

# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro

print(10 == 10)  # Sim => True (Verdadeiro)
print(10 == 11)  # Não => False (Falso)

# Classe type: determina o tipo do dado inserido no código.

print(type(True))
print(type(False))
print(type(1.1))
print(type(1))
print(type('Rocky'))


# Aqui só para mostrar que uma coisa é igual a outra.
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))