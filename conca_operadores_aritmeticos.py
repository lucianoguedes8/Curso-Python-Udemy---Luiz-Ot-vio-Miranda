# Operador de concatenação (+, porém para 'strings'):
concatenacao_1 = 'A' + 'A' + 'A'
concatenacao_2 = 'Luiz' + ' ' + 'Otávio'
concatenacao_3 = 'A' + 'B' + 'C'
concatenacao_4 = 'A' + ' ' + 'B' + 'C'
'''Caso queira colocar um espaço na concatenação, lembre de 
apertar a barra de espaço entre as aspas da string no código.''' 
print(concatenacao_1)
print(concatenacao_2)
print(concatenacao_3)
print(concatenacao_4)
print('\n')

# Operador de Concatenação de Repetição (*, utilizando int e 'strings')
a_dez_vezes = 'A' * 10
tres_vezes_luiz = 3 * 'Luiz'
print(a_dez_vezes)
print(tres_vezes_luiz)
# Não tem como utilizar float, pois se tratam de strings também.